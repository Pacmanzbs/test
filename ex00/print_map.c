#include <unistd.h>

void	print_map(char **map)
{
	int i;
	int j;

	i = 0;
	while (i < 9)
	{
		j = 0;
		while (j < 9)
		{
			write(1, &map[i][j], 1);
			++j;
			write(1, " ", 1);
		}
		write(1, "\n", 1);
		++i;
	}
}
